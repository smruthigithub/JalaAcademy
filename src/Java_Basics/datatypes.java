package Java_Basics;

public class datatypes {

	public static void main(String[] args) {

		int num = 33;
		boolean b = true;
		char c = 'k';
		float fl = 5.8f;
		double db = 5.8;
		
		System.out.println(num);
		System.out.println(b);
		System.out.println(c);
		System.out.println(fl);
		System.out.println(db);
		
	}

}
